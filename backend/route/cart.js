const express = require('express');
const router = express.Router();
const cartController = require('../controlers/cart');

router.post('/addToCart', cartController.addToCartForUser);
router.get('/getCartListData', cartController.getCartListData);
router.delete('/deleteCartList/:id', cartController.deleteCartList);
router.delete('/deleteAllCartList', cartController.deleteAllCartList);
router.put('/updateCartItem/:id', cartController.updateCartItem);


module.exports = router;
